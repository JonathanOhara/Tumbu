#include "Dialog.h"
//-------------------------------------------------------------------------------------
Dialog::Dialog(){
	answered = false;
	ok = false;
	cancel = false;
}
//-------------------------------------------------------------------------------------
Dialog::~Dialog(void){
}
//-------------------------------------------------------------------------------------
Dialog::DialogType Dialog::getType(){
	return type;
}
//-------------------------------------------------------------------------------------
void Dialog::setType(DialogType _type){
	type = _type;
}
//-------------------------------------------------------------------------------------
bool Dialog::isAnswered(){
	return answered;
}
//-------------------------------------------------------------------------------------
void Dialog::setAnswered( bool _answered ){
	answered = _answered;
}
//-------------------------------------------------------------------------------------
bool Dialog::isOk(){
	return ok;
}
//-------------------------------------------------------------------------------------
void Dialog::setOk( bool _ok ){
	ok = _ok;
}
//-------------------------------------------------------------------------------------
bool Dialog::isCancel(){
	return cancel;
}
//-------------------------------------------------------------------------------------
void Dialog::setCancel( bool _cancel ){
	cancel = _cancel;
}
//-------------------------------------------------------------------------------------